# == (czy wartości są równe)
# != (czy wartości są różne)


print(11 > 1) # czy 11 jest większe niż jeden
print(11 == 1) # czy 11 jest równe jeden
print(11 < 1) # czy 11 jest mniejsze od 1
print(11 != 1) # czy 11 jest różne niż jeden

